#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int			len;
	char		*buffer;
	int			i;

	if (!s1)
		return (NULL);
	len = ft_strlen(s1);
	buffer = malloc(len + 1);
	if (!buffer)
		return (NULL);
	i = 0;
	while (s1[i] != '\0')
	{
		buffer[i] = s1[i];
		i++;
	}
	buffer[i] = '\0';
	return (buffer);
}
